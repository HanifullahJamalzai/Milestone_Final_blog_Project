     */
    public function update(Request $request, User $user)
    {
        Gate::authorize('IsAdmin');
        $data = $request->validate([
            'role' => 'required|numeric'
        ]);

        $user->update([
            'role' => $data['role'],
        ]);

        return redirect()->route('user.index')->with('success', 'User role Updated successfully!');

